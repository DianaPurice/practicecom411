print("Towards which direction shout I paint (up, down, left or right)? ")
direction = input()

#determinate if the direction is up
if (direction == "up"): print("\nI am painting in the upward direction!")
elif (direction == "down"): print("\nI am painting in the downward direction!")
elif (direction == "left"): print("\nI am painting in the left direction!")
elif (direction == "right"): print("\nI am printing in the right direction!")
else: print("\nI am not sure which direction I am painting in!")